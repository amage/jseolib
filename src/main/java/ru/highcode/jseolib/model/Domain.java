package ru.highcode.jseolib.model;

public class Domain {
    private long id;
    private String code;
    private String name;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Domain [code=" + getCode() + ", name=" + getName() + ", id=" + getId() + "]";
    }
}
