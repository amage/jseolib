package ru.highcode.jseolib.model;

public class GroupData {
    private long id;
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "GroupData [id=" + getId() + ", name=" + getName() + "]";
    }
}
